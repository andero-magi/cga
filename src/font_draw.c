static boolean initialized = 0;
static char_callback_t callback = null;
static float textXScale = 1;
static float textYScale = 1;
static int fontTextureId = 0;

int cgaTextDrawIsInitialized() {
  return initialized;
}

boolean cgaInitTextDraw() {
  if (initialized) {
    return false;
  }

  const int imgXSize = 16 * CHAR_SIZE;
  const int imgYSize =  8 * CHAR_SIZE;
  const int imgSize = imgXSize * imgYSize;

  uint8_t* textureData[imgSize] = {0};

  for (int i = 0; i < CHARS; i++) {
    char* data = font8x8_basic[i];
  }

  glGenTextures(1, &fontTextureId);

  if (fontTextureId == 0) {
    logError("Failed to generate font texture ID");
    return false;
  }

  glBindTexture(GL_TEXTURE_2D, fontTextureId);
  glTexImage2D(GL_TEXTURE_2D, 0, GL_R8, imgXSize, imgYSize, 0, GL_RED, GL_UNSIGNED_BYTE, textureData);

  GLenum err = glGetError();

  if (err != GL_NO_ERROR) {
    char* err = glewGetErrorString(err);
    logErrorF("GL Error creating texture: %s", err);
    return false;
  }

  initialized = true;
  return true;
}

void cgaCloseTextDraw() {
  if (!initialized) {
    return;
  }

  initialized = false;

  if (fontTextureId != 0) {
    glDeleteTextures(0, &fontTextureId);
  }
}

void cgaSetTextScale(float xScale, float yScale) {
  textXScale = xScale;
  textYScale = yScale;
}

void cgaSetCharDrawCallback(char_callback_t callbackfn) {
  callback = callbackfn;
}

static int triggerCallback(char ch, int chIndex, float* x, float* y) {
  if (callback == null) {
    return true;
  }

  return callback(ch, chIndex, x, y);
}

static void drawCharAt(char bitmap[], float chx, float chy, float xscale, float yscale) {
  int set = 0;

  float startX = 0;
  float startY = 0;
  float endY = 0;
  float endX = 0;

  for (int x = 0; x < CHAR_SIZE; x++) {
    for (int y = 0; y < CHAR_SIZE; y++) {
      set = bitmap[y] & (1 << x);

      if (!set) {
        continue;
      }

      startX = chx + (x * xscale);
      startY = chy - (y * yscale);
      endX = startX + xscale;
      endY = startY - yscale;

      drawQuad(startX, startY, endX, endY);
    }  
  }
}

void cgaDrawText(float x, float y, int maxbufSize, const char* content) {
  if (!initialized) {
    return;
  }

  float charX = x;
  float charY = y;

  const float startX = x;
  const float startY = y;

  const float sizex = CH_BASE_X_SCALE * textXScale;
  const float sizey = CH_BASE_Y_SCALE * textYScale;

  int index = 0;

  while (index < maxbufSize) {
    char ch = content[index++];

    if (ch == ENDCHAR) {
      break;
    }

    if (ch == '\n' || ch == '\r') {
      charX = startX;
      charY -= CHAR_DIF_Y * sizey;
      continue;
    }

    char* chData = font8x8_basic[ch];

    if (chData == null) {
      charX += CHAR_DIF_X * sizex;
      continue;
    }

    int shouldDraw = triggerCallback(ch, index - 1, &charX, &charY);

    if (!shouldDraw) {
      continue;
    }

    drawCharAt(chData, charX, charY, sizex, sizey);
    charX += CHAR_DIF_X * sizex;
  }
}

#define max(a, b) (a < b ? b : a)

void cgaMeasureText(int maxBufSize, const char *content, float *width, float *height, int *lines) {
  float widthCounter = 0;
  float heightCounter = 0;
  float maxWidth = 0;
  int lineCount = 0;

  int index = 0;

  const float sizex = CH_BASE_X_SCALE * textXScale;
  const float sizey = CH_BASE_Y_SCALE * textYScale;

  while (index < maxBufSize) {
    char ch = content[index++];

    if (ch == ENDCHAR) {
      break;
    }

    if (ch == '\n' || ch == '\r') {
      heightCounter += CHAR_DIF_Y * sizey;
      maxWidth = max(widthCounter, maxWidth);
      lineCount++;
      widthCounter = 0;  

      continue;
    }

    widthCounter += CHAR_DIF_X * sizex;
  }

  maxWidth = max(maxWidth, widthCounter);

  if (width != null) {
    *width = maxWidth;
  }
  if (height != null) {
    if (index > 0) {
      heightCounter += CHAR_DIF_Y * sizey;
    }

    *height = heightCounter;
  }
  if (lines != null) {
    if (index > 0) {
      lineCount++;
    }

    *lines = lineCount;
  }
}
